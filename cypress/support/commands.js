Cypress.Commands.add("login", () => {
  cy.setCookie(Cypress.env("COOKIE_NAME"), Cypress.env("COOKIE"), {
    domain: "localhost",
    httpOnly: true,
    path: "/",
    secure: false,
  });
});

Cypress.Commands.add("logout", () => {
  cy.visit("/api/auth/signout");
  cy.get("form").submit();
});
